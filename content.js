// Current chapter being viewed
let currentChapter = 1;

// Load chapter content
function loadChapter(chapterNum) {
    currentChapter = chapterNum;
    const content = learningContent[chapterNum];

    // Update tabs
    document.querySelectorAll('.chapter-tab').forEach(tab => {
        tab.classList.toggle('active', parseInt(tab.dataset.chapter) === chapterNum);
    });

    // Update content
    const contentArea = document.getElementById('learning-content');
    contentArea.innerHTML = `
        <div class="content-header">
            <h2>‡∏ö‡∏ó‡∏ó‡∏µ‡πà ${chapterNum}: ${content.title}</h2>
            <p class="subtitle">${content.subtitle}</p>
        </div>
        ${content.content}
    `;

    // Update navigation
    updateChapterNav();

    // Scroll to top
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

// Update chapter navigation buttons
function updateChapterNav() {
    const prevBtn = document.getElementById('btn-prev-chapter');
    const nextBtn = document.getElementById('btn-next-chapter');

    prevBtn.disabled = currentChapter === 1;
    nextBtn.disabled = currentChapter === 5;
}

// Previous chapter
function prevChapter() {
    if (currentChapter > 1) {
        loadChapter(currentChapter - 1);
    }
}

// Next chapter
function nextChapter() {
    if (currentChapter < 5) {
        loadChapter(currentChapter + 1);
    }
}

// Show learning page
function showLearning() {
    showPage('learning');
    loadChapter(1);
}

// Initialize tab click handlers
document.addEventListener('DOMContentLoaded', function () {
    document.querySelectorAll('.chapter-tab').forEach(tab => {
        tab.addEventListener('click', function () {
            loadChapter(parseInt(this.dataset.chapter));
        });
    });
});
